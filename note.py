'''
This is a class representing a Note.
A Note has a pitch (String) and a duration (Float)
'''
class Note:
    # Constructor for note
    def __init__(self, pitch, duration):
        this.pitch = pitch
        this.duration = duration

    # Gets the pitch
    def getPitch(self):
        return this.pitch

    # Gets the duration
    def getDuration(self):
        return this.duration